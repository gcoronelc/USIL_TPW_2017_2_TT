
package pe.egcc.condominio.model;

public class Torre {
	
	private int idtorre;
	private String nombre;
	
	public Torre() {}

	public int getIdtorre() {
		return idtorre;
	}

	public void setIdtorre(int idtorre) {
		this.idtorre = idtorre;
	}

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	
}
